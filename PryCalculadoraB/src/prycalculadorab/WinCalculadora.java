    @SuppressWarnings("unchecked")
    public static String calculadora (String _memoria1, String _memoria2, String _signo){
        Double _resultado=0.0;
        String _respuesta;
        
        if (_signo.equals("-")){
            _resultado=Double.parseDouble(_memoria1)-Double.parseDouble(_memoria2);
        }
        if (_signo.equals("+")){
            _resultado=Double.parseDouble(_memoria1)+Double.parseDouble(_memoria2);
        }
        if (_signo.equals("*")){
            _resultado=Double.parseDouble(_memoria1)*Double.parseDouble(_memoria2);
        }
        if (_signo.equals("/")){
            _resultado=Double.parseDouble(_memoria1)/Double.parseDouble(_memoria2);
        }
        _respuesta=_resultado.toString();
        return _respuesta;
    }
